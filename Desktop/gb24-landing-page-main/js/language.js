let currentLanguage = 'en';

function switchLanguage(lang) {
    currentLanguage = lang;
    updateTextContent();
    updateLanguageSwitcher();
    localStorage.setItem('preferredLanguage', lang);
}

function updateTextContent() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage][key]) {
            element.textContent = translations[currentLanguage][key];
        }
    });
}

function updateLanguageSwitcher() {
    // Support multiple elements with id='languageSwitcher' (navbar and footer)
    const switchers = document.querySelectorAll('#languageSwitcher');
    switchers.forEach(switcher => {
        switcher.innerHTML = currentLanguage === 'en' ? 'French' : 'English';
    });
}

// Initialize language based on user preference or browser language
document.addEventListener('DOMContentLoaded', () => {
    const savedLanguage = localStorage.getItem('preferredLanguage');
    const browserLanguage = navigator.language.split('-')[0];

    if (savedLanguage) {
        switchLanguage(savedLanguage);
    } else if (browserLanguage === 'fr') {
        switchLanguage('fr');
    }
}); 